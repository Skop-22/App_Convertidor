def Hexadecimal(dato):
    if dato == 10:
        return "A"
    elif dato == 11:
        return "B"
    elif dato == 12:
        return "C"
    elif dato == 13:
        return "D"
    elif dato == 14:
        return "E"
    elif dato == 15:
        return "F"


